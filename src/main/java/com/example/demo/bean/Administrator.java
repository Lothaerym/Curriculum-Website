package com.example.demo.bean;

public class Administrator {

    private String pwd;
    private String name;

    public void setPwd(String pwd) {
	this.pwd = pwd;
    }
    public String getPwd() {
	return pwd;
    }

    public void setName(String name) {
	this.name = name;
    }
    public String getName() {
	return name;
    }
}